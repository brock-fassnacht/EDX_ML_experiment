# Conditional Probability

set.seed(1) 
disease <- sample(c(0,1), size=1e6, replace=TRUE, prob=c(0.98,0.02))
test <- rep(NA, 1e6)
test[disease==0] <- sample(c(0,1), size=sum(disease==0), replace=TRUE, prob=c(0.90,0.10))
test[disease==1] <- sample(c(0,1), size=sum(disease==1), replace=TRUE, prob=c(0.15, 0.85))

## Q2 probability test is positive
mean(test)

## Q3 probability has disease if test is negative
mean(disease[test==0])

## Q4 probability you have the disease if test is positive
mean(disease[test==1])

## Q5 if a patients test is positive how many more times likely are they to have the disease (prevelance)
mean(disease[test==1])/mean(disease)



## Q6 What code makes correct plot
library(dslabs)
data("heights")
heights %>% 
  mutate(height = round(height)) %>%
  group_by(height) %>%
  summarize(p = mean(sex == "Male")) %>%
qplot(height, p, data =.)


#Q7 make each group have same number of points with cut
ps <- seq(0, 1, 0.1)
heights %>% 
  mutate(g = cut(height, quantile(height, ps), include.lowest = TRUE)) %>%
  group_by(g) %>%
  summarize(p = mean(sex == "Male"), height = mean(height)) %>%
  qplot(height, p, data =.)

